export function resolveLocale(value: string | undefined | null): Locale {
  return value === "en" ? "en" : "id";
}

export function getDictionary(locale: Locale) {
  return dictionaries[locale];
}

export function formatLocalizedDate(date: Date | string, locale: Locale) {
  const parsed = typeof date === "string" ? new Date(date) : date;
  return new Intl.DateTimeFormat(localeMap[locale], {
    day: "2-digit",
    month: "long",
    year: "numeric",
  }).format(parsed);
}

export function getStatusLabel(locale: Locale, status: StatusHukum) {
  return dictionaries[locale].status[status];
}

export function getDocumentLabels(locale: Locale, type: DocumentType) {
  return dictionaries[locale].documents[type];
}
